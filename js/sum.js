/**
 * Dvieju skaiciu aritmetine suma.
 * 
 * Extra aprasas...
 * 
 * ```js
 * [11,2,22,1].sort((a, b) => a - b)
 * ```
 * @param {number} a Pirma reikšmė
 * @param {number} b Antra reikšmė
 *  @example
 * sum(5, 10); // returns 15
 * @returns {number} Dvieju skaiciu suma
 */
export function sum(a, b) {
    return a + b;
}